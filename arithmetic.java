class arithmetic
{
	public static void main(String args[])
	{
		int i=10, j=20;
		System.out.println("Addition of 2 numbers is: "+(i+j));
		System.out.println("Subtraction of 2 numbers is: "+(i-j));
		System.out.println("Multiplication of 2 numbers is: "+(i*j));
		System.out.println("Division of 2 numbers is: "+(i/j));
		System.out.println("Remainder after division is: "+(i%j));
	}
}
